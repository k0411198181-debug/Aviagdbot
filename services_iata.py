def resolve_iata(city: str) -> Optional[Tuple[str, str]]:
    """Быстрый sync поиск по локальному словарю."""
    stripped = city.strip()

    # Если уже IATA-код (3 буквы)
    if len(stripped) == 3 and stripped.isalpha():
        return (stripped.upper(), stripped.upper())

    key = stripped.lower()
    if key in CITIES:
        return CITIES[key]

    # Частичное совпадение
    for k, v in CITIES.items():
        if k.startswith(key) or key in k:
            return v
    return None


async def _search_iata_api(city: str) -> Optional[Tuple[str, str]]:
    """Fallback: Travelpayouts Autocomplete API для незнакомых городов."""
    try:
        url = "https://autocomplete.travelpayouts.com/places2"
        params = {"locale": "ru", "types[]": "city", "term": city}
        async with aiohttp.ClientSession() as s:
            async with s.get(url, params=params, timeout=aiohttp.ClientTimeout(total=8)) as r:
                if r.status != 200:
                    return None
                data = await r.json()
                if data:
                    item = data[0]
                    iata = item.get("code", "")
                    name = item.get("name", city)
                    if iata:
                        return (iata.upper(), name)
    except Exception as e:
        logger.debug("IATA autocomplete error: %s", e)
    return None


async def resolve_iata_async(city: str) -> Optional[Tuple[str, str]]:
    """
    Async резолвинг: сначала локальный словарь, потом API.
    Используй в хэндлерах вместо sync resolve_iata.
    """
    result = resolve_iata(city)
    if result:
        return result
    return await _search_iata_api(city)


def resolve_train_station(city: str) -> Optional[Tuple[str, str]]:
    """Sync поиск ЖД-станции по названию города."""
    key = city.strip().lower()
    if key in TRAIN_STATIONS:
        return TRAIN_STATIONS[key]
    for k, v in TRAIN_STATIONS.items():
        if k.startswith(key) or key in k:
            return v
    return None
